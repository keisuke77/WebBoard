    <h1>掲示板ルール</h1>
    <h2>1. 基本ルール</h2>
    <ul>
        <li><strong>礼儀を守る:</strong> 他の利用者に対して礼儀正しく接してください。攻撃的、差別的、または不適切な発言は厳禁です。</li>
        <li><strong>スパム禁止:</strong> 同じ内容の投稿を繰り返す、無関係なリンクを貼るなどのスパム行為は禁止です。</li>
        <li><strong>プライバシーの尊重:</strong> 他人の個人情報（名前、住所、電話番号など）を無断で公開しないでください。</li>
    </ul>
    <h2>2. 投稿のガイドライン</h2>
    <ul>
        <li><strong>適切なカテゴリに投稿する:</strong> 投稿は適切なカテゴリやスレッドに行ってください。</li>
        <li><strong>具体的なタイトル:</strong> 投稿のタイトルは具体的で、内容を分かりやすく表現してください。</li>
        <li><strong>内容の明確化:</strong> 投稿内容は明確で簡潔にまとめてください。誤解を招く表現や不確かな情報は避けてください。</li>
    </ul>
    <h2>3. 著作権と法的遵守</h2>
    <ul>
        <li><strong>著作権を尊重する:</strong> 他人の著作物を無断で転載しないでください。引用する場合は出典を明記してください。</li>
        <li><strong>違法行為の禁止:</strong> 法律に違反する内容の投稿は一切禁止です。</li>
    </ul>
    <h2>4. 管理とモデレーション</h2>
    <ul>
        <li><strong>モデレーターの権限:</strong> モデレーターは掲示板の秩序を保つために、投稿の編集、削除、スレッドの閉鎖などを行う権限を持ちます。</li>
        <li><strong>警告と制裁:</strong> ルール違反者には警告が発せられ、重大な場合や再犯の場合はアカウントの一時停止または永久停止が行われることがあります。</li>
    </ul>
    <h2>5. 利用者の責任</h2>
    <ul>
        <li><strong>自己責任の原則:</strong> 掲示板に投稿する内容は自己責任で行ってください。発言に対する責任は全て利用者に帰属します。</li>
        <li><strong>フィードバックと報告:</strong> 問題のある投稿や行為を見つけた場合、管理者またはモデレーターに報告してください。</li>
    </ul>
